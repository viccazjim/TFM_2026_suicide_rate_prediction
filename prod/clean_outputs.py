"""
Cleanup utility: deletes every artifact the pipeline generates, so the next
run of prod/run_pipeline.py (or the notebooks) starts from a clean slate and
nothing stale from a previous version of the code lingers around looking
like it came from the current one.

Deletes the *contents* of:
    data/processed/    (df_development.parquet, df_real_world.parquet, ...)
    outputs/figures/    (every *_*.png saved by save_figure())
    outputs/tables/      (every result/prediction table, .parquet or .csv)
    outputs/models/      (every persisted model/scaler, .joblib)
    catboost_info/        (CatBoost's own training-log directory, recreated
                            every run regardless of whether this script deletes it)

Never touches: data/raw/ (the manually-downloaded IHME source file — not
generated by this pipeline, and not something to casually delete), docs/,
notebooks/, prod/, src/, deprecated/, temp/, or anything outside the five
directories above. The four data/outputs directories themselves are kept
(only their contents are removed), so relative paths elsewhere in the
codebase that assume the directory exists keep working without needing a
mkdir first.

Usage:
    python prod/clean_outputs.py              # asks for confirmation, then deletes
    python prod/clean_outputs.py --yes         # skips confirmation
    python prod/clean_outputs.py --dry-run     # lists what would be deleted, deletes nothing

    # Keep specific directories untouched:
    python prod/clean_outputs.py --keep-data --keep-models
"""

import argparse
import logging
import shutil
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
sys.path.append(str(REPO_ROOT))

logging.basicConfig(level=logging.INFO, format="%(asctime)s | %(levelname)s | %(message)s")
logger = logging.getLogger(__name__)

TARGETS = {
    "data": REPO_ROOT / "data" / "processed",
    "figures": REPO_ROOT / "outputs" / "figures",
    "tables": REPO_ROOT / "outputs" / "tables",
    "models": REPO_ROOT / "outputs" / "models",
}
CATBOOST_INFO_DIR = REPO_ROOT / "catboost_info"


def _list_contents(directory: Path) -> list[Path]:
    """Files and subdirectories directly and indirectly under `directory`,
    deepest first, so a caller can delete files before the (now-empty)
    directories that contained them. Returns [] if `directory` doesn't exist."""
    if not directory.exists():
        return []
    return sorted(directory.rglob("*"), key=lambda p: len(p.parts), reverse=True)


def preview(keep: set[str]) -> dict[str, list[Path]]:
    """
    Builds the list of what would be deleted, without deleting anything.

    Parameters
    ----------
    keep : set[str]
        Keys from TARGETS to skip (e.g. {"data", "models"}).

    Returns
    -------
    dict[str, list[Path]]
        {target_name: [files/dirs that would be removed]}, plus a
        "catboost_info" entry if that directory currently exists.
    """
    plan = {}
    for name, directory in TARGETS.items():
        if name in keep:
            continue
        plan[name] = _list_contents(directory)
    if "catboost_info" not in keep and CATBOOST_INFO_DIR.exists():
        plan["catboost_info"] = [CATBOOST_INFO_DIR]
    return plan


def clean(keep: set[str] = frozenset(), dry_run: bool = False) -> dict[str, int]:
    """
    Deletes the contents of every target directory not in `keep`.

    Parameters
    ----------
    keep : set[str], default frozenset()
        Keys from {"data", "figures", "tables", "models", "catboost_info"}
        to leave untouched.
    dry_run : bool, default False
        If True, only logs what would be deleted; deletes nothing.

    Returns
    -------
    dict[str, int]
        {target_name: number of top-level items removed}.
    """
    plan = preview(keep)
    counts = {}

    for name, items in plan.items():
        if not items:
            counts[name] = 0
            continue

        if name == "catboost_info":
            logger.info("%s %s", "[dry-run] Would remove" if dry_run else "Removing", CATBOOST_INFO_DIR)
            if not dry_run:
                shutil.rmtree(CATBOOST_INFO_DIR)
            counts[name] = 1
            continue

        directory = TARGETS[name]
        logger.info(
            "%s %d item(s) from %s",
            "[dry-run] Would remove" if dry_run else "Removing", len(items), directory,
        )
        for item in items:
            if dry_run:
                continue
            if item.is_dir():
                shutil.rmtree(item, ignore_errors=True)
            else:
                item.unlink(missing_ok=True)
        counts[name] = len(items)

    return counts


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--yes", "-y", action="store_true", help="Skip the confirmation prompt")
    parser.add_argument("--dry-run", action="store_true", help="List what would be deleted, delete nothing")
    parser.add_argument("--keep-data", action="store_true", help="Don't touch data/processed/")
    parser.add_argument("--keep-figures", action="store_true", help="Don't touch outputs/figures/")
    parser.add_argument("--keep-tables", action="store_true", help="Don't touch outputs/tables/")
    parser.add_argument("--keep-models", action="store_true", help="Don't touch outputs/models/")
    parser.add_argument("--keep-catboost-info", action="store_true", help="Don't touch catboost_info/")
    args = parser.parse_args()

    keep = set()
    if args.keep_data:
        keep.add("data")
    if args.keep_figures:
        keep.add("figures")
    if args.keep_tables:
        keep.add("tables")
    if args.keep_models:
        keep.add("models")
    if args.keep_catboost_info:
        keep.add("catboost_info")

    plan = preview(keep)
    total = sum(len(v) for v in plan.values())
    if total == 0:
        logger.info("Nothing to clean — every target directory is already empty.")
        sys.exit(0)

    logger.info("This will delete:")
    for name, items in plan.items():
        if items:
            logger.info("  %s: %d item(s)", name, len(items))

    if args.dry_run:
        clean(keep=keep, dry_run=True)
        sys.exit(0)

    if not args.yes:
        answer = input(f"\nDelete {total} item(s) across {len([k for k, v in plan.items() if v])} director{'y' if len([k for k, v in plan.items() if v]) == 1 else 'ies'}? [y/N] ")
        if answer.strip().lower() not in ("y", "yes"):
            logger.info("Aborted — nothing was deleted.")
            sys.exit(0)

    counts = clean(keep=keep, dry_run=False)
    logger.info("Done. Removed: %s", counts)
